mylist = ['Python', 'Java', 'C++', 'JavaScript']
print(mylist)
print(type(mylist),'\n')  # <class 'list'>

# Allows duplicate values
mylist = ['Python', 'Java', 'C++', 'JavaScript', 'Python']
print('Allow Duplicate Values',mylist,'\n')

# List Length
mylist = ['Python', 'Java', 'C++', 'JavaScript']
print('Length List: ',len(mylist),'\n')  # 4

# List Constructors
mylist = list(('Python', 'Java', 'C++', 'JavaScript'))
print('List Constructor: ',mylist,'\n')  # ['Python', 'Java', 'C++', 'JavaScript']


