# Remove Specified Item
myarr = ["python", "java", "c++", "javascript"]
myarr.remove("java")  # Remove "java" from the list
print('Remove specific Item: ',myarr) 

myarr1 = ["python", "java", "c++", "javascript","python"]
myarr1.remove("python")
print('Remove Same Data: ',myarr1)  # Remove the first occurrence of "python" from the list


# Remove Specified Index
myarr3 = ["python", "java", "c++", "javascript"]
myarr3.pop(2)  # Remove the item at index 2
print('Remove Values index: ',myarr3)  # Remove the item at index 2


