#Upper case
username = "akash"
print('Upper Case:',username.upper())

#Lower Case
fullname = "AKASH"
print('Lower Case:',fullname.lower())

#Capitalize Case 
print('Capitalized:',fullname.capitalize())

#Remove Spaces
address = "123 Main Street"
print('Address:',address.strip())

#Replace String
username1 = "Akash Biswas"
print('Replace:',username1.replace("Akash","Amit"))

#Split String
array = "Python, Java, C++"
print(array)
print('Split:',array.split(","))
