x0 = int(1)   # x will be 1
y0 = int(2.8) # y will be 2
z0 = int("3") # z will be 3

print("FOR INTEGER")
print(x0 , y0 , z0,"\n")

x1 = float(1)     # x will be 1.0
y1 = float(2.8)   # y will be 2.8
z1 = float("3")   # z will be 3.0
w1 = float("4.2") # w will be 4.2

print("FOR FLOAT")
print(x1,y1,z1,w1,"\n")


x2 = str("s1") # x will be 's1'
y2 = str(2)    # y will be '2'
z2 = str(3.0)  # z will be '3.0' 

print("FOR STRING")
print(x2,y2,z2)
print(type(x2),type(y2),type(z2))