var = 25
var1 = 10
print("Addition:", var + var1)  # Addition
print("Subtraction:", var - var1)  # Subtraction
print("Multiplication:", var * var1)  # Multiplication
print("Division:", var / var1)  # Division
print("Floor Division:", var // var1)  # Floor Division
print("Modulus:", var % var1)  # Modulus
print("Exponent:", var1 ** 2 )  # Exponent
print("Bitwise AND:", var & var1)  # Bitwise AND
print("Bitwise OR:", var | var1)  # Bitwise OR
print("Bitwise XOR:", var ^ var1)  # Bitwise XOR
print("Bitwise NOT:", ~var)  # Bitwise NOT
print("Left Shift:", var << 2)  # Left Shift
print("Right Shift:", var >> 2)  # Right Shift
print("Identity:", var is var1)  # Identity
print("Membership:", var in [1, 2, 3, 4, 5])  # Membership
print("Logical AND:", var > 20 and var1 < 15)  # Logical AND
print("Logical OR:", var > 20 or var1 < 5)  # Logical OR
print("Logical NOT:", not (var > 20))  # Logical NOT
print("Ternary Operator:", "Greater" if var > var1 else "Smaller")  # Ternary Operator
